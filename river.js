function checkIfOceanIsNearby(x, y, pole) {
	if(x + 1 < pole.length && pole[x + 1][y] == 0) {
    	return true;
	}

	if(x - 1 >= 0 && pole[x - 1][y] == 0) {
    	return true;
	}
	
	if (y + 1 < pole[x].length && x + 1 < pole.length && pole[x + 1][y + 1] == 0) {
		return true;
	}

	if (y - 1 >= 0 && x + 1 < pole.length && pole[x + 1][y - 1] == 0) {
		return true;
	}
    
	if (y + 1 < pole[x].length && x - 1 >= 0 && pole[x - 1][y + 1] == 0) {
		return true;
	}
    
	if (y - 1 >= 0 && x - 1 >= 0 && pole[x - 1][y - 1] == 0) {
		return true;
	}
        
	if(y + 1 < pole[x].length && pole[x][y + 1] == 0) {
		return true;
	}

	if(y - 1 >= 0 && pole[x][y - 1] == 0) {
		return true;
	}
    return false;
}

